export function daysToMil(days: number) {
    return days * 24 * 60 * 60 * 1000;
}
export function hoursToMil(hours: number) {
    return hours * 60 * 60 * 1000;
}
export function minToMil(minutes: number) {
    return minutes * 60 * 1000;
}
export function secToMil(seconds: number) {
    return seconds * 1000;
}
